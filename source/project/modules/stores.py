class Store:
    def __init__(self, store_id, store_name, phone, email, street, city, state, zip_code):
        self.store_id = store_id
        self.store_name = store_name
        self.phone = phone
        self.email = email
        self.street = street
        self.city = city
        self.state = state
        self.zip_code = zip_code