class Staff:
    def __init__(self, staff_id, first_name, last_name, phone, email, active, store_id, manager_id):
        self.staff_id = staff_id
        self.first_name = first_name
        self.last_name = last_name
        self.phone = phone
        self.email = email
        self.active = active
        self.store_id = store_id
        self.manager_id = manager_id
